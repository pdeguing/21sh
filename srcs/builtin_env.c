#include "shell.h"

int		builtin_env(char **args)
{
	int		i;

	if (args[0])
		ft_printf("");
	i = 0;
	while (g_env[i])
	{
		ft_printf("%s\n", g_env[i]);
		i++;
	}
	return (0);
}
